#!groovy
node {
    stage('Git checkout') { // for display purposes
        git 'https://github.com/jaymutuku/e2e-test-automation-pipeline.git'
    }
    stage('Smoke') {
        try {
            sh "./gradlew clean test -Ptags='type:Smoke'"
